public class ClassUtil {
    public static <T> T getInstance(Class cls) {
        try {
            return (T) cls.newInstance();
        } catch (InstantiationException e) {
            e.printStackTrace();
        } catch (IllegalAccessException e) {
            e.printStackTrace();
        }
        return null;
    }
}
